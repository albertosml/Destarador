    private void boton_electorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_electorActionPerformed
        JFileChooser elector = new JFileChooser();
        elector.setFileFilter(new FileNameExtensionFilter("Imágenes","jpeg","jpg","gif","bmp","png"));
        int r = elector.showOpenDialog(null);
        File f = elector.getSelectedFile();
        if(r == JFileChooser.APPROVE_OPTION) {
            nombre_archivo = f.getAbsolutePath();
            ruta.setText(nombre_archivo);
            if(!ruta.getText().isEmpty()) boton_imagen.setVisible(true);
        }
    }//GEN-LAST:event_boton_electorActionPerformed

    private void boton_imagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_imagenActionPerformed
        String path = ruta.getText();
        if(!path.isEmpty()) {
            OperacionesBD o = new OperacionesBD();
            try {
                o.modificarCaja(path,codigoCaja,3);
                boton_borrar.setVisible(true);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ModificarCaja.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane,"El cambio de imagen de la caja se ha realizado con éxito");
        }
    }//GEN-LAST:event_boton_imagenActionPerformed

    private void boton_cambiar_nombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_cambiar_nombreActionPerformed
        String name = nombre.getText();
        if(!name.isEmpty()) {
            OperacionesBD o = new OperacionesBD();
            try {
                o.modificarCaja(name,codigoCaja,1);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ModificarCaja.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane,"El cambio de nombre de la caja se ha realizado con éxito");
        }
        else JOptionPane.showMessageDialog(rootPane,"Introduzca un nombre");
    }//GEN-LAST:event_boton_cambiar_nombreActionPerformed

    private void boton_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_borrarActionPerformed
        OperacionesBD o = new OperacionesBD();
        try {
            o.modificarCaja("",codigoCaja,3);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ModificarCaja.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(rootPane,"Se ha borrado la imagen de la caja");
        
        ruta.setText("");
        boton_borrar.setVisible(false);
        boton_imagen.setVisible(false);
    }//GEN-LAST:event_boton_borrarActionPerformed

