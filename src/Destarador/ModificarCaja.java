    private void boton_cambiar_pesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_cambiar_pesoActionPerformed
        String p = peso.getText();
        if(p.matches("[0-9]+[.][0-9]+") || p.matches("[0-9]+")) {
            OperacionesBD o = new OperacionesBD();
            try {
                o.modificarCaja(p,codigoCaja,2);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ModificarCaja.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane,"El cambio de peso de la caja se ha realizado con éxito");
        }
        else JOptionPane.showMessageDialog(rootPane,"El peso puede ser un número entero (135) ó decimal (135.46)");
    }//GEN-LAST:event_boton_cambiar_pesoActionPerformed

